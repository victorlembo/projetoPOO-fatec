    private void btnNumCpfCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNumCpfCliActionPerformed
        int i;
        String cpfFormatted;
        for (i = 0; i < cadCliVend.size(); i++) {
            if (cadCliVend.get(i) instanceof Cliente) {
                if (((Pessoa) cadCliVend.get(i)).getCpf().equals(txtCpfCli.getText())) {

                    break;
                }
            }
        }

        cpfFormatted = txtCpfCli.getText().replace("-", "").replace(".", "");
        if (Pessoa.validarCPF(cpfFormatted)) {
            if (i < cadCliVend.size()) {
                posCli = i;
            } else {
                posCli = -1;
            }

            if (posCli >= 0) {
                p = new Pedido(txtNumPedido.getText(),
                        txtDataPedido.getText());
                txtCpfCli.setText(((Cliente) cadCliVend.get(posCli)).getCpf());
                lblNomeCliente.setText(((Cliente) cadCliVend.get(posCli)).getNome());
                p.setCliente((Cliente) cadCliVend.get(posCli));

                txtDataPedido.setEnabled(false);
                txtCpfCli.setEnabled(false);
                btnNumCpfCli.setEnabled(false);
                txtCpfVend.setEnabled(true);
                btnNumCpfVend.setEnabled(true);
                txtCpfVend.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                txtCpfCli.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "CPF Inválido!");
            txtCpfCli.requestFocus();
        }
    }//GEN-LAST:event_btnNumCpfCliActionPerformed

