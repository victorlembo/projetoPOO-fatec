    private void btnNumPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNumPedidoActionPerformed
        String numPed = txtNumPedido.getText();

        if (isNumeric(numPed)) {
            int x;
            for (x = 0; x < cadPed.size(); x++) {
                if (((Pedido) cadPed.get(x)).getNumero().equals(txtNumPedido.getText())) {
                    break;
                }
            }
            if (x < cadPed.size()) {
                posPed = x;
            } else {
                posPed = -1;
            }

            if (posPed >= 0) {
                txtNumPedido.setText((cadPed.get(posPed)).getNumero());
                txtDataPedido.setText((cadPed.get(posPed)).getDataEmissao());
                cbxFormaPagamento.setSelectedIndex(0);

                txtCpfCli.setText((cadCliVend.get(posCli)).getCpf());
                lblNomeCliente.setText((cadCliVend.get(posCli)).getNome());
                txtCpfVend.setText((cadCliVend.get(posVend)).getCpf());
                lblNomeVend.setText((cadCliVend.get(posVend)).getNome());

                for (int i = 0; i < cadItemPed.size(); i++) {
                    String row[] = {
                        cadPed.get(posPed).getItensPedido(i).getProduto().getCodigo(),
                        cadPed.get(posPed).getItensPedido(i).getProduto().getDescricao(),
                        String.valueOf(cadPed.get(posPed).getItensPedido(i).getProduto().getPreco()),
                        String.valueOf(cadPed.get(posPed).getItensPedido(i).getQtdeVendida()),
                        String.valueOf((cadPed.get(posPed).getItensPedido(i).getProduto().getPreco() * cadPed.get(posPed).getItensPedido(i).getQtdeVendida()))

                    };

                    modTblItemPedido.addRow(row);
                    valTotal += (cadPed.get(posPed).getItensPedido(i).getProduto().getPreco() * cadPed.get(posPed).getItensPedido(i).getQtdeVendida());
                    qtdItensPed += cadPed.get(posPed).getItensPedido(i).getQtdeVendida();
                }

                lblValTotal.setText(String.valueOf(valTotal));
                lblQtdeItensPed.setText(String.valueOf(qtdItensPed));

                txtNumPedido.setEnabled(false);
                btnNumPedido.setEnabled(false);
                txtDataPedido.setEnabled(true);
                cbxFormaPagamento.setEnabled(true);
                txtCpfCli.setEnabled(true);
                btnNumCpfCli.setEnabled(true);
                txtCpfVend.setEnabled(true);
                btnNumCpfVend.setEnabled(true);
                txtCodProd.setEnabled(true);
                btnNumCodProd.setEnabled(true);
                txtQtdeVendida.setEnabled(true);

                btnAdicionarItem.setEnabled(true);
                btnRemoverItem.setEnabled(true);
                btnAlterar.setEnabled(true);
                btnExcluir.setEnabled(true);
            } else {
                btnNumPedido.setEnabled(false);
                txtDataPedido.setEnabled(true);
                cbxFormaPagamento.setEnabled(true);
                txtCpfCli.setEnabled(true);
                btnNumCpfCli.setEnabled(true);
                txtDataPedido.requestFocus();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Digite um código válido!");
            txtNumPedido.requestFocus();
        }


    }//GEN-LAST:event_btnNumPedidoActionPerformed

